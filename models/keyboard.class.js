/**
 * Handles keyboard input state for the game
 */
class Keyboard {
    /** @type {boolean} Left arrow key state */
    LEFT = false;
    /** @type {boolean} Right arrow key state */
    RIGHT = false;
    /** @type {boolean} Up arrow key state */
    UP = false;
    /** @type {boolean} Down arrow key state */
    DOWN = false;
    /** @type {boolean} Spacebar key state */
    SPACE = false;
    /** @type {boolean} D key state */
    D = false;
}
